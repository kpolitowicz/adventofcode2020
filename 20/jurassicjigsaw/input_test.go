package jurassicjigsaw

import (
	"reflect"
	"testing"
)

func TestParseInput(t *testing.T) {
	got := ParseInput(`Tile 2311:
..##.#..#.
##..#.....
#...##..#.
####.#...#
##.##.###.
##...#.###
.#.#.#..##
..#....#..
###...#.#.
..###..###

Tile 1951:
#.##...##.
#.####...#
.....#..##
#...######
.##.#....#
.###.#####
###.##.##.
.###....#.
..#.#..#.#
#...##.#..`)
	want := &imageMap{
		"2311": {
			"..##.#..#.",
			"##..#.....",
			"#...##..#.",
			"####.#...#",
			"##.##.###.",
			"##...#.###",
			".#.#.#..##",
			"..#....#..",
			"###...#.#.",
			"..###..###",
		},
		"1951": {
			"#.##...##.",
			"#.####...#",
			".....#..##",
			"#...######",
			".##.#....#",
			".###.#####",
			"###.##.##.",
			".###....#.",
			"..#.#..#.#",
			"#...##.#..",
		},
	}

	if !reflect.DeepEqual(got, want) {
		t.Errorf("got %v want %v", got, want)
	}
}
